    private void ingresarBTMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_ingresarBTMouseClicked
        // TODO add your handling code here

        String usuario = userTF.getText();
        String contraseña = String.valueOf(passwordTF.getPassword());
        String archivoUsuarios = "./usuarios.txt";
        String rol = "";
        String nombre = "";
        String apellido = "";
        String sueldo = "";
        String id = "";
        String carrera = "";
        String usuarioActual = "";
        String contraseñaActual = "";

        try {
            boolean encontrado;
            try ( FileReader fr = new FileReader(archivoUsuarios);  BufferedReader br = new BufferedReader(fr)) {
                String linea;
                encontrado = false;
                while ((linea = br.readLine()) != null) {
                    var partes = linea.split("\\|");

                    rol = partes[2];

                    if (rol.equals("alumno")) {
                        usuarioActual = partes[0];
                        contraseñaActual = partes[1];
                        nombre = partes[3];
                        apellido = partes[4];
                        id = partes[5];
                        carrera = partes[6];
                    } else if (rol.equals("docente")) {
                        usuarioActual = partes[0];
                        contraseñaActual = partes[1];
                        nombre = partes[3];
                        apellido = partes[4];
                        id = partes[5];
                        carrera = partes[6];
                        sueldo = partes[7];
                    } else if(rol.equals("registro")) {
                        usuarioActual = partes[0];
                        contraseñaActual = partes[1];
                        nombre = partes[3];
                        apellido = partes[4];
                        id = partes[5];
                    }

                    if (usuarioActual.equals(usuario) && contraseñaActual.equals(contraseña)) {
                        encontrado = true;
                        break;
                    }
                }
            }

            if (encontrado) {
                switch (rol) {
                    case "registro" -> {
                        bg.setVisible(false);
                        docentesP.setVisible(false);
                        registroP.setVisible(true);
                        bienvenidaTXT.setText("¡Hola " + nombre + " " + apellido + "!");

                    }
                    case "alumno" -> {
                        bg.setVisible(false);
                        docentesP.setVisible(false);
                        registroP.setVisible(false);

                    }
                    case "docente" -> {
                        bg.setVisible(false);
                        registroP.setVisible(false);
                        docentesP.setVisible(false);

                    }
                    default -> {
                    }
                }
            } else {
                badLoginTXT.setVisible(true);
            }

        } catch (IOException e) {
        }
    }//GEN-LAST:event_ingresarBTMouseClicked

